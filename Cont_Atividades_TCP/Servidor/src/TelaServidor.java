    private void btnAtivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtivarActionPerformed
        btnAtivar.setEnabled(false);
        lblServidor.setText("ATIVO");
        lblServidor.setForeground(Color.blue);
        addLog("Servidor Ativado");
        
        listaUsuarios = new ArrayList();
        listaAtividades = new ArrayList();
        
        ReceiveMessage recebendo = new ReceiveMessage(txtLog,listaUsuarios,listaAtividades);
        recebendo.start();
        
        EncAtividade encaminhando = new EncAtividade(listaUsuarios, listaAtividades,txtLog);
        encaminhando.start();
        
    }//GEN-LAST:event_btnAtivarActionPerformed
    
    public void addLog(String msg){
        Date data = new Date();
        txtLog.append(data.getHours() + ":" + data.getMinutes() + ":" + data.getSeconds() + " - " + msg + "\n");
    }
    
